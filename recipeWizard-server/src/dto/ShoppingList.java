package dto;

import java.io.Serializable;

/**
 *
 * @author Agnesss
 */
public class ShoppingList implements Serializable{
    
    private int id;
    private String name;

    public ShoppingList() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
    
    
    
}
